const walkFiles = (dirPath) => {
  const entries = fs.readdirSync(dirPath, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dirPath, entry.name);
    if (entry.isDirectory()) {
      files.push(...walkFiles(fullPath));
      continue;
    }
    files.push(fullPath);
  }

  return files;
};

const normalizeKey = (value) =>
  value
    .replace(/__+/g, "_")
    .replace(/^_+|_+$/g, "")
    .replace(/[^a-z0-9_]/g, "")
    .replace(/^(\d)/, "item_$1");

const ruLocale = readJson(ruPath);
const kkLocale = readJson(kkPath);
const keys = Object.keys(ruLocale);

const used = new Set();
const keyMap = {};

for (const oldKey of keys) {
  if (keyOverrides[oldKey]) {
    const fixed = normalizeKey(keyOverrides[oldKey]);
    let unique = fixed;
    let i = 2;
    while (used.has(unique)) {
      unique = `${fixed}_${i}`;
      i += 1;
    }
    used.add(unique);
    keyMap[oldKey] = unique;
    continue;
  }

  const translatedTokens = oldKey.split("_").map((token) => {
    if (/^\d+$/.test(token)) {
      return token;
    }
    return tokenMap[token] ?? token;
  });

  const base = normalizeKey(translatedTokens.join("_")) || "text";
  let unique = base;
  let i = 2;
  while (used.has(unique)) {
    unique = `${base}_${i}`;
    i += 1;
  }

  used.add(unique);
  keyMap[oldKey] = unique;
}

const renamedRu = {};
const renamedKk = {};
for (const oldKey of keys) {
  const newKey = keyMap[oldKey];
  renamedRu[newKey] = ruLocale[oldKey];
  renamedKk[newKey] = kkLocale[oldKey];
}

writeJson(ruPath, renamedRu);
writeJson(kkPath, renamedKk);

const filesToUpdate = walkFiles(srcDir).filter((filePath) => {
  const isLocale = filePath === ruPath || filePath === kkPath;
  if (isLocale) {
    return false;
  }
  return /\.(ts|tsx|js|jsx|json)$/.test(filePath);
});

for (const filePath of filesToUpdate) {
  const original = fs.readFileSync(filePath, "utf8");
  let updated = original;

  for (const oldKey of keys) {
    const newKey = keyMap[oldKey];
    const pattern = new RegExp(`([\"'\\\`])${escapeRegExp(oldKey)}\\1`, "g");
    updated = updated.replace(pattern, (_, quote) => `${quote}${newKey}${quote}`);
  }

  if (updated !== original) {
    fs.writeFileSync(filePath, updated, "utf8");
  }
}

console.log(`Renamed ${keys.length} locale keys to English-style names.`);
